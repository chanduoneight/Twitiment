def generate_sentiment_pdf(text: str, scores: dict, word_data: list, aspect_data: list) -> bytes:
    """Generate a neat PDF report of the sentiment analysis."""
    pdf = FPDF()
    pdf.add_page()
    
    safe_main_text = pdf_safe_text(text)
    
    # Title
    pdf.set_font("Arial", "B", 20)
    pdf.set_text_color(63, 81, 181)
    pdf.cell(0, 15, "Sentiment Intelligence Report", ln=True, align="C")
    pdf.ln(5)
    
    # Content Section
    pdf.set_font("Arial", "B", 14)
    pdf.set_text_color(0, 0, 0)
    pdf.cell(0, 10, "Analyzed Content:", ln=True)
    pdf.set_font("Arial", "", 11)
    pdf.multi_cell(0, 8, safe_main_text)
    pdf.ln(10)
    
    # Overall Sentiment
    pdf.set_font("Arial", "B", 14)
    pdf.cell(0, 10, "Overall Sentiment Analysis:", ln=True)
    pdf.set_font("Arial", "", 12)
    pdf.cell(0, 8, f"Verdict: {pdf_safe_text(scores['label'])}", ln=True)
    pdf.cell(0, 8, f"Compound Polarity Score: {scores['compound']:.4f}", ln=True)
    pdf.ln(5)
    
    # Score Breakdown Table
    pdf.set_font("Arial", "B", 12)
    pdf.cell(60, 10, "Metric", 1)
    pdf.cell(60, 10, "Score", 1, ln=True)
    pdf.set_font("Arial", "", 11)
    for k, v in [("Positive", scores["pos"]), ("Neutral", scores["neu"]), ("Negative", scores["neg"])]:
        pdf.cell(60, 8, k, 1)
        pdf.cell(60, 8, f"{v:.3f}", 1, ln=True)
    pdf.ln(10)
    
    # Word-Level (Top 10)
    if word_data:
        pdf.set_font("Arial", "B", 14)
        pdf.cell(0, 10, "Key Token Sentiment (Top 10):", ln=True)
        pdf.set_font("Arial", "B", 10)
        pdf.cell(50, 8, "Token", 1)
        pdf.cell(30, 8, "Score", 1)
        pdf.cell(40, 8, "Sentiment", 1, ln=True)
        pdf.set_font("Arial", "", 10)
        for w in word_data[:10]:
            pdf.cell(50, 7, pdf_safe_text(w["word"]), 1)
            pdf.cell(30, 7, str(round(w["compound"], 3)), 1)
            pdf.cell(40, 7, pdf_safe_text(w["label"]), 1, ln=True)

    # Footer
    pdf.set_y(-25)
    pdf.set_font("Arial", "I", 8)
    pdf.set_text_color(128, 128, 128)
    report_time = time.strftime('%Y-%m-%d %H:%M:%S')
    pdf.cell(0, 10, f"Generated by Sentiment Intelligence Dashboard | {report_time}", align="C")
    
    # ✅ Fixed: proper encoding for string to bytes conversion
    return pdf.output(dest="S").encode("latin-1")
