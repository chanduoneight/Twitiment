def word_bar_chart(word_data: list[dict]) -> go.Figure:
    """Horizontal bar chart of word-level compound scores."""
    if not word_data:
        return go.Figure()
    df = pd.DataFrame(word_data)
    colors = df["compound"].apply(
        lambda s: "#50fa7b" if s >= 0.05 else ("#ff5555" if s <= -0.05 else "#f1fa8c")
    )
    fig = go.Figure(
        go.Bar(
            x=df["compound"],
            y=df["word"],
            orientation="h",
            marker=dict(
                color=colors,
                line=dict(color="rgba(255,255,255,0.1)", width=1)
            ),
            text=df["compound"].round(3),
            textposition="outside",
            textfont={"color": "#f8f8f2", "family": "Inter"},
        )
    )
    fig.update_layout(
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(0,0,0,0)",
        font={"color": "#f8f8f2", "family": "Inter"},
        xaxis=dict(
            title="Compound Score",
            range=[-1.1, 1.1],
            gridcolor="rgba(255,255,255,0.05)",
            zerolinecolor="rgba(255,255,255,0.2)"
        ),
        yaxis=dict(title="", gridcolor="rgba(255,255,255,0.05)"),
        margin=dict(t=10, b=10, l=10, r=50),
        height=max(200, len(word_data) * 32 + 40),
    )
    return fig


