# --------------------------------------------------------------------------- #
# PDF text cleaner
# --------------------------------------------------------------------------- #
def pdf_safe_text(text: str) -> str:
    """Clean text of emojis and non-Latin1 characters for FPDF compatibility."""
    import emoji
    # Remove emojis
    text = emoji.replace_emoji(text, replace="")
    # Replace common curly quotes/dashes with ASCII equivalents
    replacements = {
        "\u2018": "'", "\u2019": "'", "\u201c": '"', "\u201d": '"',
        "\u2013": "-", "\u2014": "-", "\u2026": "...", "\u2022": "|"
    }
    for k, v in replacements.items():
        text = text.replace(k, v)
    # Encode and decode as latin-1 to strip any remaining non-compatible characters
    return text.encode("latin-1", "ignore").decode("latin-1")

