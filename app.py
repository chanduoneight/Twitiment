@st.cache_data(show_spinner=False)
def get_text_cached(key, lang_display, default=None):
    """Cached version of translation helper."""
    # Extract language name from string like "🇸 English" or "🇯🇵 日本語 (Japanese)"
    lang_name = "English"
    if "(" in lang_display:
        lang_name = lang_display.split("(")[-1].replace(")", "").strip()
    else:
        parts = lang_display.split()
        if len(parts) > 1:
            lang_name = parts[-1]
    
    lang_code = LANG_CODE_MAP.get(lang_name, "English")
    translations = TRANSLATIONS.get(lang_code, TRANSLATIONS["English"])
    return translations.get(key, default or key)

def get_text(key, default=None):
    """Get translated text (wraps cached function)."""
    lang_display = st.session_state.get("language_filter", "🇸 English")
    return get_text_cached(key, lang_display, default)


