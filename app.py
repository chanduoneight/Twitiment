def get_live_tweets(count=5, use_vader=True):
    """Generate simulated live tweets with real-time VADER sentiment analysis."""
    tweets = []
    for _ in range(count):
        # Select random template
        tweet_text = random.choice(LIVE_TWEET_TEMPLATES)
        
        # Add random trending hashtag
        if random.random() > 0.5:
            tweet_text += f" {random.choice(TRENDING_HASHTAGS)}"
        
        # Perform real sentiment analysis using VADER
        if use_vader:
            sentiment_result = sentence_sentiment(tweet_text)
            compound = sentiment_result['compound']
            sentiment_label = sentiment_result['label']
            pos_score = sentiment_result['pos']
            neu_score = sentiment_result['neu']
            neg_score = sentiment_result['neg']
        else:
            compound = random.uniform(-1, 1)
            sentiment_label = 'Positive' if compound >= 0.05 else ('Negative' if compound <= -0.05 else 'Neutral')
            pos_score = random.uniform(0, 0.5)
            neu_score = random.uniform(0.3, 0.7)
            neg_score = random.uniform(0, 0.5)
        
        
        # Select random location
        location_name = random.choice(list(LOCATION_DATA.keys()))
        location_info = LOCATION_DATA[location_name]
        
        # Generate realistic username
        username = f'@{random.choice(USER_TEMPLATES)}_{random.randint(10, 999)}'
        
        tweet = {
            'Username': username,
            'Tweet': tweet_text,
            'Sentiment': sentiment_label,
            'Compound': round(compound, 4),
            'Pos_Score': round(pos_score, 3),
            'Neu_Score': round(neu_score, 3),
            'Neg_Score': round(neg_score, 3),
            'Timestamp': pd.Timestamp.now() - pd.Timedelta(seconds=random.randint(1, 120)),
            'Location': location_name,
            'Country': location_info['country'],
            'Lat': location_info['lat'],
            'Lon': location_info['lon'],
            'Retweets': random.randint(0, 500),
            'Likes': random.randint(0, 2000),
            'Replies': random.randint(0, 200),
            'Verified': random.random() > 0.8,
        }
        tweets.append(tweet)
        
        # Update session state counters
        st.session_state.total_tweets += 1
        if sentiment_label == 'Positive':
            st.session_state.positive_count += 1
        elif sentiment_label == 'Negative':
            st.session_state.negative_count += 1
        else:
            st.session_state.neutral_count += 1
    
    # Add to history (keep last 100 tweets)
    st.session_state.tweet_history.extend(tweets)
    st.session_state.tweet_history = st.session_state.tweet_history[-100:]
    
    # Update sentiment timeline
    for tweet in tweets:
        st.session_state.sentiment_timeline.append({
            'timestamp': tweet['Timestamp'],
            'compound': tweet['Compound'],
            'sentiment': tweet['Sentiment']
        })
    st.session_state.sentiment_timeline = st.session_state.sentiment_timeline[-500:]
    
    st.session_state.last_update = pd.Timestamp.now()
    
    return tweets

