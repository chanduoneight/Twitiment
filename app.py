def create_sentiment_donut(positive=50, neutral=25, negative=25):
    """Create sentiment donut chart with X brand colors."""
    # X brand colors: blue for primary, retweet green, like pink
    colors = ['#00ba7c', '#71767b', '#f91880']  # Green, Gray, Pink (X style)
    fig = go.Figure(data=[go.Pie(
        labels=['Positive', 'Neutral', 'Negative'],
        values=[positive, neutral, negative],
        hole=0.6,
        marker_colors=colors,
        textinfo='percent',
        textfont=dict(size=14, color='#e7e9ea', family='Inter'),
        hovertemplate='<b>%{label}</b><br>%{value}%<extra></extra>'
    )])
    
    # Add center text
    fig.add_annotation(
        text=f'<b>{positive}%</b>',
        showarrow=False,
        font=dict(size=24, color='#00ba7c', family='Inter')
    )
    
    fig.update_layout(
        showlegend=False,
        paper_bgcolor='rgba(0,0,0,0)',
        plot_bgcolor='rgba(0,0,0,0)',
        margin=dict(t=5, b=5, l=5, r=5),
        height=180
    )
    return fig


